package com.example.arimaa2;

/**
 * Represents the types of game pieces.
 * It can be one of the following:
 * - ELEPHANT
 * - CAMEL
 * - HORSE
 * - DOG
 * - CAT
 * - RABBIT
 */
public enum PieceType {
    /**
     * Represents the type elephant.
     */
    ELEPHANT,

    /**
     * Represents the type camel.
     */
    CAMEL,

    /**
     * Represents the type horse.
     */
    HORSE,

    /**
     * Represents the type dog.
     */
    DOG,

    /**
     * Represents the type cat.
     */
    CAT,

    /**
     * Represents the type rabbit.
     */
    RABBIT
}

