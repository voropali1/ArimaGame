package com.example.arimaa2;

/**
 * Represents the status of a game.
 */
public enum GameStatus {
    /**
     * Indicates that the game has started.
     */
    START,

    /**
     * Indicates that the game is in progress.
     */
    PROCESS,

    /**
     * Indicates that the game has ended.
     */
    END
}
